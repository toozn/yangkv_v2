Status DecodeMemEntry(Slice& s, MemEntry& entry) {
	uint32_t klength, vlength;
	const char* ptr = s.data();
	const char* key_ptr = GetVarint32Ptr(ptr, ptr + 5, &klength);
	entry.key = Slice(key_ptr, klength - 9);
	uint64_t info = DecodeFixed64(key_ptr + klength - 8);
	entry.seq_num = info >> 8;
	entry.value_type = info & 255;
	const char* vptr = key_ptr + klength;
	vptr = GetVarint32Ptr(vptr, vptr + 5, &vlength);
	entry.value = Slice(vptr, vlength - 1);
	return Status::OK();
}

void EncodeMemEntry(MemEntry& entry, Slice& s) {

	uint64_t key_size = entry.key.size() + 1;
	uint64_t val_size = entry.value.size() + 1;
	uint64_t internal_key_size = key_size + 8;
	const uint64_t encoded_len =
	VarintLength(internal_key_size) + internal_key_size + VarintLength(val_size) + val_size;
	char* buf = new char[encoded_len];
	char* p = EncodeVarint32(buf, internal_key_size);
	memcpy(p, entry.key.data(), key_size);
	p += key_size;
	EncodeFixed64(p, (entry.seq_num << 8) | entry.value_type);
	p += 8;
	p = EncodeVarint32(p, val_size);
	memcpy(p, entry.value.data(), val_size);
  s = Slice(buf, encoded_len);
	assert(p + val_size == buf + encoded_len);
  
  
}

LookupKey::LookupKey(Slice& key, uint64_t seq_num) {
	char* dst = new char[key.size() + 13];
	start_ = dst;
	dst = EncodeVarint32(dst, key.size() + 8);
	key_start_ = dst;
	memcpy(dst, key.data(), key.size());
	EncodeFixed64(dst, (seq_num << 8) | 1);
	end_ = dst + 8;
}


void EncodeFixed32(char* buf, uint32_t value) {
  if (1) {
    memcpy(buf, &value, sizeof(value));
  } else {
    buf[0] = value & 0xff;
    buf[1] = (value >> 8) & 0xff;
    buf[2] = (value >> 16) & 0xff;
    buf[3] = (value >> 24) & 0xff;
  }
}

void EncodeFixed64(char* buf, uint64_t value) {
  if (1) {
    memcpy(buf, &value, sizeof(value));
  } else {
    buf[0] = value & 0xff;
    buf[1] = (value >> 8) & 0xff;
    buf[2] = (value >> 16) & 0xff;
    buf[3] = (value >> 24) & 0xff;
    buf[4] = (value >> 32) & 0xff;
    buf[5] = (value >> 40) & 0xff;
    buf[6] = (value >> 48) & 0xff;
    buf[7] = (value >> 56) & 0xff;
  }
}

void PutFixed32(std::string* dst, uint32_t value) {
  char buf[sizeof(value)];
  EncodeFixed32(buf, value);
  dst->append(buf, sizeof(buf));
}

void PutFixed64(std::string* dst, uint64_t value) {
  char buf[sizeof(value)];
  EncodeFixed64(buf, value);
  dst->append(buf, sizeof(buf));
}

char* EncodeVarint32(char* dst, uint32_t v) {
  // Operate on characters as unsigneds
  unsigned char* ptr = reinterpret_cast<unsigned char*>(dst);
  static const int B = 128;
  if (v < (1<<7)) {
    *(ptr++) = v;
  } else if (v < (1<<14)) {
    *(ptr++) = v | B;
    *(ptr++) = v>>7;
  } else if (v < (1<<21)) {
    *(ptr++) = v | B;
    *(ptr++) = (v>>7) | B;
    *(ptr++) = v>>14;
  } else if (v < (1<<28)) {
    *(ptr++) = v | B;
    *(ptr++) = (v>>7) | B;
    *(ptr++) = (v>>14) | B;
    *(ptr++) = v>>21;
  } else {
    *(ptr++) = v | B;
    *(ptr++) = (v>>7) | B;
    *(ptr++) = (v>>14) | B;
    *(ptr++) = (v>>21) | B;
    *(ptr++) = v>>28;
  }
  return reinterpret_cast<char*>(ptr);
}

void PutVarint32(std::string* dst, uint32_t v) {
  char buf[5];
  char* ptr = EncodeVarint32(buf, v);
  dst->append(buf, ptr - buf);
}

char* EncodeVarint64(char* dst, uint64_t v) {
  static const int B = 128;
  unsigned char* ptr = reinterpret_cast<unsigned char*>(dst);
  while (v >= B) {
    *(ptr++) = (v & (B-1)) | B;
    v >>= 7;
  }
  *(ptr++) = static_cast<unsigned char>(v);
  return reinterpret_cast<char*>(ptr);
}

void PutVarint64(std::string* dst, uint64_t v) {
  char buf[10];
  char* ptr = EncodeVarint64(buf, v);
  dst->append(buf, ptr - buf);
}

void PutLengthPrefixedSlice(std::string* dst, const Slice& value) {
  PutVarint32(dst, value.size());
  dst->append(value.data(), value.size());
}

int VarintLength(uint64_t v) {
  int len = 1;
  while (v >= 128) {
    v >>= 7;
    len++;
  }
  return len;
}

const char* GetVarint32PtrFallback(const char* p,
                                   const char* limit,
                                   uint32_t* value) {
  uint32_t result = 0;
  for (uint32_t shift = 0; shift <= 28 && p < limit; shift += 7) {
    uint32_t byte = *(reinterpret_cast<const unsigned char*>(p));
    p++;
    if (byte & 128) {
      // More bytes are present
      result |= ((byte & 127) << shift);
    } else {
      result |= (byte << shift);
      *value = result;
      return reinterpret_cast<const char*>(p);
    }
  }
  return nullptr;
}

bool GetVarint32(Slice* input, uint32_t* value) {
  const char* p = input->data();
  const char* limit = p + input->size();
  const char* q = GetVarint32Ptr(p, limit, value);
  if (q == nullptr) {
    return false;
  } else {
    *input = Slice(q, limit - q);
    return true;
  }
}

